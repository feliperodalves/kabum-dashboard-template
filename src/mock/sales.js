const yearsFilter = () => {
  const tempYears = [];
  sales.forEach(sale => {
    const yearExists = tempYears.find(year => sale.year === year);
    const index = tempYears.findIndex(year => sale.year < year);
    if (!yearExists) {
      if (index === -1) {
        tempYears.push(sale.year);
      } else {
        tempYears.splice(index, 0, sale.year);
      }
    }
  });
  return tempYears;
};

const categoriesFilter = () => {
  const tempCategories = [];
  let count = 1;
  sales.forEach(sale => {
    const categoryExists = tempCategories.find(
      category => sale.name === category.name,
    );
    const index = tempCategories.findIndex(
      category => sale.name < category.name,
    );
    if (!categoryExists) {
      if (index === -1) {
        tempCategories.push({ id: count, name: sale.name });
      } else {
        tempCategories.splice(index, 0, { id: count, name: sale.name });
      }
      count += 1;
    }
  });
  return tempCategories;
};

